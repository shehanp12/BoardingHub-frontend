class BoardingProvider {
  String fullName = '';
  String email = '';
  String contactNumber = '';
  String userName = '';
  String address = '';
  String password = '';
  String address = '';
  String contactNumber = '';

  BoardingProvider(
    String fullName,
    String email,
    String contactNumber,
    String userName,
    String address,
    String password,
  ) {
    this.fullName = fullName;
    this.email = email;
    this.contactNumber = contactNumber;
    this.userName = userName;
    this.address = address;
    this.password = password;
    this.address= address;
    this.contactNumber= contactNumber;
  }
}
